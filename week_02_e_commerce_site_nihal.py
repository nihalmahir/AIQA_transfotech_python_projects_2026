# ----------------- Product --------------------
# Simple product class: products can be added with only a name and class

class Product:

  def __init__(self, name,price):  #initialize
    self.name = name
    self.price = price

# ----------------- Cart ----------------------
#Simple cart class : product objects can only be
# added to the cart class' list(empty at first, like an empty cart),
# or removed.
#you can also check total price of products

class Cart:
  def __init__(self):   #initialize
    self.items = []

  def add_item(self,product):
    self.items.append(product)

  def remove_item(self,product):
    self.items.remove(product)

  def check_total(self):
    total = 0
    for item in self.items:
      total += item.price
    print(total)


# ---------------- Implementation ----------------
