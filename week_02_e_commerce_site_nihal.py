while True:
  print("\n================ 😎 GLAMAZON 🤩 ====================")
  print(" 1 ==> View Products " )
  print(" 2 ==> Add Product to Cart " )
  print(" 3 ==> Remove Product from Cart " )
  print(" 4 ==> View Total " )
  print(" 5 ==> Checkout " )
  print(" 6 ==> Exit " )

  choice = input ("Enter an option to start glammin! ")

  #User starts shopping interactively
  # View Products
  if choice == "1":
    print("\n Catalogue:")
    for product in products:
      product.display_product()

  # Add product to cart
  elif choice == "2":
    product_id = int( input("Enter Product ID to add:") )

    found = False
    for product in products:
      if product.product_id == product_id:
        cart.add_item(product)
        print(f"✓{product.name} added to cart.")
        found = True
        break
      if not found:
        print ("❌ Oops! product ID not found!")

  # Remove Product from Cart
  elif choice == "3":
    product_id = int( input("Enter Product ID to remove:") )
    cart.remove_item(product_id)

  # View Total
  elif choice == "4":
    print(f"\n Cart total : ${cart.check_total():.2f}")

  #Checkout
  elif choice == "5":
    cart.checkout()

  #Exit
  elif choice == "6":
    print("Thanks for visiting Glamazon, stay glammin!")
    break

  #Validation : Wrong menu option
  else:
    print ("Invalid option. Please enter a number from the menu")

